begin-module turtle-tree

  turtle import
  
  defer tree ( size level -- )
  :noname { size level }
    level 0> if
      size forward
      60 left
      size 2 * 3 / level 1- tree
      120 right
      size 2 * 3 / level 1- tree
      60 left
      size negate forward
    then
  ; is tree

end-module
